                <p class="text-justify">To have a successful program there must be understanding and cooperation among parents, swimmers, and coaches. The progress your child makes depends to a great extent on this triangular relationship. The following guidelines will help you keep your child’s development as an athlete in the proper perspective and help your child reach his/her full potential under the coach's direction. Ten and Under: Ten and under are the most inconsistent swimmers and this can be frustrating for parents, coaches and the swimmer alike! Parents and coaches must be patient and permit these youngsters to learn to love the sport. When a young swimmer first joins, there may be a brief period in which he/she appears to slow down. This is a result of the added concentration on stroke technique, but this will soon lead to much faster swims for the individual. Even the very best swimmer will have meets where they do not do their best times. These “plateaus” are a normal part of swimming. Over the course of a season, times should improve. Please be supportive of these “poor” meets. The older swimmers may have only two or three meets a year for which they will be rested and tapered. Sportsmanlike behavior and improved performance are of equal importance. All the coaches teach swimmers how to behave like champions whether the swimmers have a “good” or a “bad” swim. Respect for officials, congratulations to other competitors, encouragement to teammates, determined effort, and mature attitudes are examples of great behavior. A swimmer should be praised for improving his/her stroke or time. It is the coach’s job to offer constructive criticism of a swimmer’s performance. It is the parent’s responsibility to provide love and encouragement that bolster the swimmer’s confidence along the way. Swimmers are taught to set realistic, yet challenging goals for meets and to relate those goals to practice to direct their training efforts. Swimmers are prepared and encouraged to compete in all swimming events, distances, and strokes. This promotes versatility and encourages the swimmer to explore his potential in the wide range of events offered in competitive swimming. Often times, a swimmer’s “best” stroke changes as they mature and his/her body goes through physical changes.</p>
